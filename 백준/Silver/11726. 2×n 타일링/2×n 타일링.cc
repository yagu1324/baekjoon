// 중점 2. 가로블럭은 이론상 2칸마다 한개씩 늘어나 경우의 수가 늘어난다!
// 중점 3. 그렇다면 nCr(조합식) 을 통해서 경우의 수를 알 수 있지 않을까?
// 중점 4. 가능하다! 그렇다면 이 조합식을 어떻게 짜면 좋을까? 
// -> nCr => r==가로x가로 블럭의 갯수, n==전체 블럭길이(입력값)-가로블럭의 갯수!
// 마지막!
// 이제 반복문을 통해 i%2==0값 마다 위의 조합식을 사용해 값을 더하고, 전부 세로일 때의 경우의수 +1을 하면 값이 나옵니다.
// 그런데 이게 답이 아님!!!!! 애초에 이렇게 풀라고 만든 문제가 아니었습니다!
