// 이런식으로 1,2 를 제외한 나머지 수들의 값은 특정값 이전 두 수의 합입니다! 
// 그렇기 때문에 int a,b를 만들고 서로 값을 왔다 갔다하며 원하는 수 까지 더하면 답이 나옵니다
// 
// 비상!!!!
// 44에서 서로 다른값을 나타냄!!!!!!!!!
// 이유) longlong 에서 최대값을 나타내는 방법이 43번까지 였습니다! 
