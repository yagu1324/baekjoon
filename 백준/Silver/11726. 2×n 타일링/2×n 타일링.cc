long long Combination(int a, int b) {// nCr 조합식을 위한 부분
	// 예외 처리
	if (a < b) return 0; //n값이 r값보다 작으면 경우의 수는 0개
	if (b == 0) return 1; //r값이 0이면 경우의 수는 1개입니다

	// nCr = nC(n-r) 성질을 이용한 최적화
	if (a - b < b) {//r 의 값만큼 반복하는데 더 적게 반복하기 위한 조건문
		b = a - b;
	}

	long long x = 1, y = 1; // 저장할 값 초기화

	for (int i = 0; i < b; i++) { // r번 만큼 반복하고 n값과 r값을 -1씩 빼가면서 곱해감
		x *= (a - i);
		y *= (b - i);
	}
	return x / y;//마지막에 서로 나누어서 값을 리턴
}

int main() {
	// 백준 시간 초과(TLE) 방지용 입출력 속도 향상 코드
	ios_base::sync_with_stdio(false);
	int a;
	long cnt = 0;//직사각형을 채울 수 있는 방법의 갯수ㅑ
	int comcnt = 1;//조합식을 위한 카운트(반복문을 통해 점점 늘어날 예정)
	cin >> a;
	if (a <= 43) { //문제가 틀렸지만 이렇게라도 남기고싶었습니다 long long 최대값입니다
		for (int i = 1; i<=a; i++) {
			if (i == 1) {
				cnt++; continue; // i 값이 1일 경우는 모두 세로로 서있을 떄의 경우의 수 입니다.
			}
		
			if (i % 2 == 0) {//i의 값이 2로 나누어 떨어졌을 경우, 가로x가로 로 두는 블럭(정사각형) 의 경우의 수를 구합니다.
				cnt =(cnt+ Combination(a-comcnt, comcnt));// 조합식을 통한 가로x가로 블럭이(comcnt)개 있을 경우의 수를 cnt에 저장
				comcnt++;//가로x가로 블럭 추가
			}
		}
		cnt %= 10007;
	}
	else {
		long long one = 1, two = 2;//초기값
		for (int i = 2; i < a; i++) {//1,2 값은 이미 정해졌으니까 3부터 시작합니다
			if (i % 2 == 0) {//i값이 2로 나누어 떨어지면 one 값에 저장
				one = (one+two)%10007; //문제에서 10007을 나눈 나머지 값을 적으라함
			}
			else {//나누어 떨어지지 않으면 two 값에 저장
				two = (one + two) % 10007; //문제에서 10007을 나눈 나머지 값을 적으라함
			}
		}
		if (a % 2 != 0)	cnt = one; //a 값이 홀수일 경우
		else	cnt = two;//a값이 짝수일 경우

	}
	


	cout << cnt;
	return 0;
}